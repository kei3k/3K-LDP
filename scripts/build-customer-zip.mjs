/**
 * Build customer-distributable zip with Unix executable permissions
 * baked-in for *.command files (so Mac double-click works without chmod).
 *
 * Auto-syncs working tree → dist folder first so the zip never goes stale.
 *
 * Usage:  node scripts/build-customer-zip.mjs
 * Output: D:/Download/3K-LDP-AI-Video-Tool.zip
 */
import { readFileSync, statSync, readdirSync, existsSync, mkdirSync, copyFileSync, rmSync } from 'fs';
import { join, relative, sep, dirname } from 'path';
import { writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import JSZip from 'jszip';

const __dirname = dirname(fileURLToPath(import.meta.url));
const WORKTREE = join(__dirname, '..');
const SRC = 'D:/Download/3K-LDP-AI-Video-Tool';
const OUT = 'D:/Download/3K-LDP-AI-Video-Tool.zip';

// Files/dirs to keep in SRC (not overwritten by sync)
const KEEP_IN_DIST = new Set(['vertex-key.json', '.env']);
// Dirs/files to skip when syncing FROM worktree — by name, matches at ANY depth
const SKIP_FROM_WORKTREE = new Set([
  '.git', '.claude', 'node_modules', 'dist', '.github', '.eslintcache',
]);
// Specific root-level files to skip (legacy junk at repo root, NOT inside src/)
const SKIP_ROOT_FILES = new Set([
  'buydash.html', 'dealworldnow.html',
  'template1_raw.html', 'template2_raw.html',  // root-level duplicates, real ones in src/lib/templates/
  'convert_to_pke.mjs', 'install_and_run.bat', 'start.sh',
  'Dockerfile', 'docker-compose.yml',
  'template1.md', 'template2.md', 'test.md', 'README.md', 'jsconfig.json',
  'test-cleaned.pke',
]);
const SKIP_EXT = new Set(['.log', '.zip']);
// Root-level extensions to skip
const SKIP_ROOT_EXT = new Set(['.pke']);
// Sub-paths to skip (forward-slash normalized)
const SKIP_PATHS = new Set(['public/ffmpeg']);

function norm(p) { return p.split(sep).join('/'); }

function shouldSkip(name, rel, isRoot) {
  if (SKIP_FROM_WORKTREE.has(name)) return true;
  if (isRoot && SKIP_ROOT_FILES.has(name)) return true;
  if (SKIP_PATHS.has(norm(rel))) return true;
  const dot = name.lastIndexOf('.');
  if (dot > 0) {
    const ext = name.substring(dot);
    if (SKIP_EXT.has(ext)) return true;
    if (isRoot && SKIP_ROOT_EXT.has(ext)) return true;
  }
  return false;
}

function syncTree(srcRoot, destRoot, relPath = '') {
  const srcDir = join(srcRoot, relPath);
  for (const name of readdirSync(srcDir)) {
    const rel = relPath ? join(relPath, name) : name;
    if (shouldSkip(name, rel, relPath === '')) continue;
    const srcPath = join(srcDir, name);
    const destPath = join(destRoot, rel);
    const st = statSync(srcPath);
    if (st.isDirectory()) {
      if (!existsSync(destPath)) mkdirSync(destPath, { recursive: true });
      syncTree(srcRoot, destRoot, rel);
    } else {
      // Don't overwrite keys/env in dist
      if (relPath === '' && KEEP_IN_DIST.has(name) && existsSync(destPath)) continue;
      copyFileSync(srcPath, destPath);
    }
  }
}

function purgeStale(destRoot, srcRoot, relPath = '') {
  // Remove anything in dest that isn't in src (except KEEP_IN_DIST root files)
  const destDir = join(destRoot, relPath);
  if (!existsSync(destDir)) return;
  for (const name of readdirSync(destDir)) {
    const rel = relPath ? join(relPath, name) : name;
    if (relPath === '' && KEEP_IN_DIST.has(name)) continue;
    // Skip ffmpeg dir — generated by postinstall, not in worktree
    if (norm(rel) === 'public/ffmpeg') continue;
    const destPath = join(destDir, name);
    const srcPath = join(srcRoot, rel);
    if (!existsSync(srcPath) || shouldSkip(name, rel, relPath === '')) {
      rmSync(destPath, { recursive: true, force: true });
      console.log(`  -  removed stale ${norm(rel)}`);
      continue;
    }
    const st = statSync(destPath);
    if (st.isDirectory()) purgeStale(destRoot, srcRoot, rel);
  }
}

console.log(`Syncing worktree → ${SRC}...`);
if (!existsSync(SRC)) mkdirSync(SRC, { recursive: true });
purgeStale(SRC, WORKTREE);
syncTree(WORKTREE, SRC);
// Always remove dist's public/ffmpeg — customer regenerates via postinstall
const distFfmpeg = join(SRC, 'public/ffmpeg');
if (existsSync(distFfmpeg)) {
  rmSync(distFfmpeg, { recursive: true, force: true });
  console.log('  -  removed public/ffmpeg (regenerated on customer install)');
}
console.log('Sync done.\n');

if (!existsSync(SRC)) {
  console.error(`Source folder not found: ${SRC}`);
  process.exit(1);
}

const zip = new JSZip();

// Recursively walk and add files
function walk(dir, baseDir = SRC) {
  for (const name of readdirSync(dir)) {
    const full = join(dir, name);
    const st = statSync(full);
    if (st.isDirectory()) {
      walk(full, baseDir);
    } else {
      const rel = relative(baseDir, full).split(sep).join('/');
      const content = readFileSync(full);
      // Unix permissions: 0755 for *.command/*.sh (executable), 0644 for others
      const isExec = /\.(command|sh)$/i.test(name);
      const unixPerm = isExec ? 0o755 : 0o644;
      zip.file(rel, content, {
        unixPermissions: unixPerm,
        date: st.mtime,
      });
      if (isExec) console.log(`  +x  ${rel}`);
    }
  }
}

console.log(`Building zip from ${SRC}...`);
walk(SRC);

const buf = await zip.generateAsync({
  type: 'nodebuffer',
  compression: 'DEFLATE',
  compressionOptions: { level: 9 },
  platform: 'UNIX', // CRITICAL: write Unix-style permissions in zip headers
});

writeFileSync(OUT, buf);
const sizeMB = (statSync(OUT).size / 1024 / 1024).toFixed(2);
console.log(`\n✓ Zip ready: ${OUT}  (${sizeMB} MB)`);
console.log('Mac users can now double-click .command files without chmod.');
